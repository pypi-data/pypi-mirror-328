    import ipset_c
    obj = ipset_c.IPSet(data)
    with pytest.raises(TypeError):
        obj.isContainsCidr(cidr)
    with pytest.raises(TypeError):
        obj.isIntersectsCidr(cidr)
    with pytest.raises(TypeError):
        obj.addCidr(cidr)
    with pytest.raises(TypeError):
        obj.removeCidr(cidr)


